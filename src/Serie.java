public class Serie {
    private final int noSerie;
    private final String nomSerie;

    public Serie(int noSerie, String nomSerie) {
        this.noSerie = noSerie;
        this.nomSerie = nomSerie;
    }

    public int getNoSerie() {
        return noSerie;
    }

    public String getNomSerie() {
        return nomSerie;
    }
}
